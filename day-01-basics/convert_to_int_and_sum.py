# Question 7: Write a Python program to take 2 numbers as input convert them to integers and print their sum.
num1 = float(input("Enter first number:"))
num2 = float(input("Enter secnd number:"))
int_num1 = int(num1)
print("First number as integer:", int_num1)
int_num2 = int(num2)
print("Second number as integer:", int_num2)
sum = int_num1 + int_num2
print("Sum:", sum)